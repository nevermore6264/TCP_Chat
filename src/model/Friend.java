package model;

/**
 *
 * @author Admin
 */
public class Friend {

    private int id;
    private String userName;
    
    public Friend(){
    }

    public Friend(int id, String userName) {
        this.id = id;
        this.userName = userName;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Friend{" +
                "id=" + id +
                ", userName='" + userName + '\'' +
                '}';
    }
}
